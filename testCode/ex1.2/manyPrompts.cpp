#include <iostream>
using namespace std;

int main()
{
   //Vars
   char fullName[256];
   int age;
   float GPA;
   char grade;

   //cout rules
   cout.setf(ios::fixed);
   cout.setf(ios::showpoint);
   cout.precision(1);

   //Prompt user for age
   cout << "How old are you?\t";
   cin >> age;

   //fullname prompt
   cout << "What is your full name?\t";
   cin.getline(fullName, 256);

   //GPA prompt
   cout << "What is your GPA?\t";
   cin >> GPA;

   //Grade prompt
   cout << "My dude, what grade are ye gonna get in this class?\t";
   cin >> grade;


   //Output results
   cout << "\n\t" << fullName
        << ", you are "
        << age << " with a GPA of "
        << GPA << "."
        << " You best be getting a(n) " << grade << " in this course boi.\n";
   return 0;
}
